package org.pa.dto;

import java.io.Serializable;

/**
 *
 * @author mwave
 */
public class UserDTO implements Serializable {
    
    String email;
    String token;

    public UserDTO() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
    
    
    
}
